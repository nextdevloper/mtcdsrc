package org.x.material.entity;

import java.math.BigDecimal;

import org.seasar.doma.Column;
import org.seasar.doma.Entity;
import org.seasar.doma.Id;
import org.seasar.doma.Table;

/**
 * コードマスタ	 コードマスタ.
 *
 * @author  nakada
 * @since   XXXXXXXX.01.00
 * @version 20141029.00.01
 */
@Entity(listener = MCodeListener.class)
@Table(name = "m_code")
public class MCode {

    /** コード種別. */
    @Id
    @Column(name = "code_kind")
    private String codeKind;

    /** コード種別名. */
    @Column(name = "code_kind_nm")
    private String codeKindNm;

    /** 表示順. */
    @Column(name = "display_no")
    private BigDecimal displayNo;

    /** コードID. */
    @Id
    @Column(name = "code_id")
    private String codeId;

    /** コード名称. */
    @Column(name = "code_nm")
    private String codeNm;

    /** コード名略称. */
    @Column(name = "code_nmr")
    private String codeNmr;

    /** 文字列1. */
    @Column(name = "text1")
    private String text1;

    /** 文字列2. */
    @Column(name = "text2")
    private String text2;

    /** 文字列3. */
    @Column(name = "text3")
    private String text3;

    /** 値1. */
    @Column(name = "value1")
    private BigDecimal value1;

    /** 値2. */
    @Column(name = "value2")
    private BigDecimal value2;

    /** 値3. */
    @Column(name = "value3")
    private BigDecimal value3;

    /** 備考. */
    @Column(name = "note")
    private String note;

    /** レコードID	 レコード編集用ID. */
    @Column(name = "rec_id")
    private Long recId;

    /**
     * Returns the codeKind.
     *
     * @return the codeKind
     */
    public final String getCodeKind() {
        return codeKind;
    }

    /**
     * Sets the codeKind.
     *
     * @param prmCodeKind the codeKind
     */
    public final void setCodeKind(final String prmCodeKind) {
        this.codeKind = prmCodeKind;
    }

    /**
     * Returns the codeKindNm.
     *
     * @return the codeKindNm
     */
    public final String getCodeKindNm() {
        return codeKindNm;
    }

    /**
     * Sets the codeKindNm.
     *
     * @param prmCodeKindNm the codeKindNm
     */
    public final void setCodeKindNm(final String prmCodeKindNm) {
        this.codeKindNm = prmCodeKindNm;
    }

    /**
     * Returns the displayNo.
     *
     * @return the displayNo
     */
    public final BigDecimal getDisplayNo() {
        return displayNo;
    }

    /**
     * Sets the displayNo.
     *
     * @param prmDisplayNo the displayNo
     */
    public final void setDisplayNo(final BigDecimal prmDisplayNo) {
        this.displayNo = prmDisplayNo;
    }

    /**
     * Returns the codeId.
     *
     * @return the codeId
     */
    public final String getCodeId() {
        return codeId;
    }

    /**
     * Sets the codeId.
     *
     * @param prmCodeId the codeId
     */
    public final void setCodeId(final String prmCodeId) {
        this.codeId = prmCodeId;
    }

    /**
     * Returns the codeNm.
     *
     * @return the codeNm
     */
    public final String getCodeNm() {
        return codeNm;
    }

    /**
     * Sets the codeNm.
     *
     * @param prmCodeNm the codeNm
     */
    public final void setCodeNm(final String prmCodeNm) {
        this.codeNm = prmCodeNm;
    }

    /**
     * Returns the codeNmr.
     *
     * @return the codeNmr
     */
    public final String getCodeNmr() {
        return codeNmr;
    }

    /**
     * Sets the codeNmr.
     *
     * @param prmCodeNmr the codeNmr
     */
    public final void setCodeNmr(final String prmCodeNmr) {
        this.codeNmr = prmCodeNmr;
    }

    /**
     * Returns the text1.
     *
     * @return the text1
     */
    public final String getText1() {
        return text1;
    }

    /**
     * Sets the text1.
     *
     * @param prmText1 the text1
     */
    public final void setText1(final String prmText1) {
        this.text1 = prmText1;
    }

    /**
     * Returns the text2.
     *
     * @return the text2
     */
    public final String getText2() {
        return text2;
    }

    /**
     * Sets the text2.
     *
     * @param prmText2 the text2
     */
    public final void setText2(final String prmText2) {
        this.text2 = prmText2;
    }

    /**
     * Returns the text3.
     *
     * @return the text3
     */
    public final String getText3() {
        return text3;
    }

    /**
     * Sets the text3.
     *
     * @param prmText3 the text3
     */
    public final void setText3(final String prmText3) {
        this.text3 = prmText3;
    }

    /**
     * Returns the value1.
     *
     * @return the value1
     */
    public final BigDecimal getValue1() {
        return value1;
    }

    /**
     * Sets the value1.
     *
     * @param prmValue1 the value1
     */
    public final void setValue1(final BigDecimal prmValue1) {
        this.value1 = prmValue1;
    }

    /**
     * Returns the value2.
     *
     * @return the value2
     */
    public final BigDecimal getValue2() {
        return value2;
    }

    /**
     * Sets the value2.
     *
     * @param prmValue2 the value2
     */
    public final void setValue2(final BigDecimal prmValue2) {
        this.value2 = prmValue2;
    }

    /**
     * Returns the value3.
     *
     * @return the value3
     */
    public final BigDecimal getValue3() {
        return value3;
    }

    /**
     * Sets the value3.
     *
     * @param prmValue3 the value3
     */
    public final void setValue3(final BigDecimal prmValue3) {
        this.value3 = prmValue3;
    }

    /**
     * Returns the note.
     *
     * @return the note
     */
    public final String getNote() {
        return note;
    }

    /**
     * Sets the note.
     *
     * @param prmNote the note
     */
    public final void setNote(final String prmNote) {
        this.note = prmNote;
    }

    /**
     * Returns the recId.
     *
     * @return the recId
     */
    public final Long getRecId() {
        return recId;
    }

    /**
     * Sets the recId.
     *
     * @param prmRecId the recId
     */
    public final void setRecId(final Long prmRecId) {
        this.recId = prmRecId;
    }
}